package com.homedepot.is.as.dto;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("reportIds")
public class ReadReportIdsDTO {
	@XStreamAlias("DomainId")
	private String domainId = null;
	@XStreamAlias("ProjectId")
	private String projectId = null;
	@XStreamAlias("FMSId")
	private String fmsId = null;
	@XStreamAlias("SubProjectId")
	private String subProjId = null;
	@XStreamAlias("TargetId")
	private String targetId = null;
	@XStreamAlias("DBName")
	private String dbName = null;
	
	public String getDomainId() {
		return domainId;
	}
	public void setDomainId(String domainId) {
		this.domainId = domainId;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getFmsId() {
		return fmsId;
	}
	public void setFmsId(String fmsId) {
		this.fmsId = fmsId;
	}
	public String getSubProjId() {
		return subProjId;
	}
	public void setSubProjId(String subProjId) {
		this.subProjId = subProjId;
	}
	public String getTargetId() {
		return targetId;
	}
	public void setTargetId(String targetId) {
		this.targetId = targetId;
	}
	public String getDbName() {
		return dbName;
	}
	public void setDbName(String dbName) {
		this.dbName = dbName;
	}
	
}
