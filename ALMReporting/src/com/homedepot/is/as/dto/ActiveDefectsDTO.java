package com.homedepot.is.as.dto;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("ActiveDefect")
public class ActiveDefectsDTO {
	@XStreamAlias("SlNo")
	private int serialNumber;
	@XStreamAlias("DefectNum")
	private int defectNumber;
	@XStreamAlias("Severity")
	private String severity;
	@XStreamAlias("Desc")
	private String defectDescription;
	@XStreamAlias("Status")
	private String status;
	@XStreamAlias("BusinessImpact")
	private String businessImpact;
	
	public int getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(int serialNumber) {
		this.serialNumber = serialNumber;
	}
	public int getDefectNumber() {
		return defectNumber;
	}
	public void setDefectNumber(int defectNumber) {
		this.defectNumber = defectNumber;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getDefectDescription() {
		return defectDescription;
	}
	public void setDefectDescription(String defectDescription) {
		this.defectDescription = defectDescription;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getBusinessImpact() {
		return businessImpact;
	}
	public void setBusinessImpact(String businessImpact) {
		this.businessImpact = businessImpact;
	}
}
